    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        double base = Double.parseDouble(txtBase.getText());
        double altura = Double.parseDouble(txtAltura.getText());
        double ladoI = Double.parseDouble(txtLadoIzq.getText());
        double ladoD = Double.parseDouble(txtLadoDer.getText());

        triangle.setBase(base);
        triangle.setHeight(altura);
        triangle.setSideL(ladoI);
        triangle.setSideR(ladoD);

        txtArea.setText("Area: " + triangle.calculateArea());
        txtPerimetro.setText("Perimetro: " + triangle.calculatePerimeter());        // TODO add your handling code here:
    }//GEN-LAST:event_btnCalcularActionPerformed

